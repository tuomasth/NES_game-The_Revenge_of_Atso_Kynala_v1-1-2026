NMI:					; NON-MASKABLE INTERRUPT
	INC TIMER
	LDA #$00
	STA $2003			; LOW BYTE RAM ADDRESS
	LDA #$02
	STA $4014			; HI BYTE RAM ADDRESS

	LDA BOOLPAUSE
	CMP #$01
	BNE GOCLOCKDECRSEC

	BEQ DRAWTIMEETC

GOCLOCKDECRSEC:
	JSR CLOCKDECRSEC

DRAWTIMEETC:
	JSR DRAWTIME
	JSR DRAWSCORE
	JSR DRAWLIVES
	JSR DRAWLEVEL
	LDA #%10001000			; PPU, THE PICTURE PROCESSING UNIT
	STA $2000
	LDA #%00011110			; COLOR
	STA $2001
	LDA #$00			; NO SCROLLING
	STA $2005
	STA $2005			; 2 TIMES TO 2005 INTENTIONALLY
	JSR CONTROLLER

;;; THE GAME ENGINE:

START:					; UNPAUSING OR PAUSING THE GAME
	LDA BUTTONS
	AND #%00010000
	BEQ UP
	LDA BOOLPAUSE
	CMP #$01
	BEQ UNPAUSING
	JSR ASKPAUSE
	JMP UP

UNPAUSING:
	JSR UNPAUSE

UP:	LDA BOOLPAUSE		; THESE 3 LINES SHOULD BE AT: TIME, BUTTONS, ENEMIES ETC.
	CMP #$01		; THESE 3 LINES
	BEQ CONTROLDONE		; THESE 3 LINES
	LDA BUTTONS
	AND #%00001000
	BEQ DN
	JSR MOVEUP
DN:	LDA BOOLPAUSE
	CMP #$01
	BEQ CONTROLDONE
	LDA BUTTONS
	AND #%00000100
	BEQ LE
	JSR MOVEDOWN
LE:	LDA BOOLPAUSE
	CMP #$01
	BEQ CONTROLDONE
	LDA BUTTONS
	AND #%00000010
	BEQ RI
	JSR MOVELEFT
RI:	LDA BOOLPAUSE
	CMP #$01
	BEQ CONTROLDONE
	LDA BUTTONS
	AND #%00000001
	BEQ CONTROLDONE
	JSR MOVERIGHT
CONTROLDONE:
UP2:	LDA BOOLPAUSE
	CMP #$01
	BEQ CONTROLDONE2
	LDA BUTTONS2
	AND #%00001000
	BEQ DN2
	JSR MOVEUP2
DN2:	LDA BOOLPAUSE
	CMP #$01
	BEQ CONTROLDONE2
	LDA BUTTONS2
